#define WM_TRAYICON     (WM_APP + 1)   // 托盘图标鼠标事件回调
#define IDM_TRAY_TOGGLE 1001            // 显示/隐藏窗口
#define IDM_TRAY_QUIT   1002            // 退出应用

// Data
ID3D11Device*            g_pd3dDevice = nullptr;
ID3D11DeviceContext*     g_pd3dDeviceContext = nullptr;
